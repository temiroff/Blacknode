from __future__ import annotations

import json
import keyword
import re
from pprint import pformat
from typing import Any, Mapping

from ..workflow import WorkflowRunError, infer_entrypoint, validate_workflow


def export_framework_scaffold(
    data: Mapping[str, Any],
    target_id: str,
    label: str,
) -> tuple[str, list[str]]:
    report = validate_workflow(data)
    if not report.ok:
        raise WorkflowRunError(json.dumps(report.to_dict(), indent=2))

    entry_node, entry_port = infer_entrypoint(data)
    projection = _workflow_projection(data, {"node_id": entry_node, "port": entry_port})
    builder_name = {
        "crewai": "build_crewai_tasks",
        "autogen": "build_autogen_agents",
        "swarm": "build_swarm_handoffs",
        "nvidia-agent-stack": "build_nvidia_agent_stack_manifest",
    }[target_id]
    body = {
        "crewai": _crewai_builder(),
        "autogen": _autogen_builder(),
        "swarm": _swarm_builder(),
        "nvidia-agent-stack": _nvidia_agent_stack_builder(),
    }[target_id]
    workflow_name = str(projection["name"])
    entry_name = str(projection.get("entrypoint_node_name") or projection["entrypoint"]["node_id"])

    code = "\n".join([
        "from __future__ import annotations",
        "",
        f"# Generated by Blacknode from workflow: {workflow_name}",
        f"# Target: {label}",
        f"# Entrypoint: {entry_name}.{projection['entrypoint']['port']}",
        "# Keep the visual graph as the source of truth, then regenerate this file when the graph changes.",
        "",
        "from pprint import pprint",
        "",
        "",
        f"WORKFLOW = {pformat(projection, width=100, sort_dicts=False)}",
        "",
        "",
        body,
        "",
        "",
        "if __name__ == '__main__':",
        f"    pprint({builder_name}())",
        "",
    ])
    return code, [f"{label} export carries graph structure; add framework-specific model clients and policies in the generated file."]


def _workflow_projection(data: Mapping[str, Any], entrypoint: dict[str, str]) -> dict[str, Any]:
    node_meta = data.get("node_meta") or {}
    nodes = {
        str(node_id): dict(meta)
        for node_id, meta in node_meta.items()
        if isinstance(meta, Mapping)
    }
    node_names = _unique_node_names(nodes)
    raw_edges = [dict(edge) for edge in data.get("edges") or []]
    incoming = _incoming_edges(raw_edges)
    outgoing = _outgoing_edges(raw_edges)
    return {
        "name": data.get("name") or "Blacknode Workflow",
        "entrypoint": entrypoint,
        "entrypoint_node_name": node_names.get(entrypoint["node_id"], entrypoint["node_id"]),
        "mcp": {
            "stdio": "blacknode mcp",
            "streamable_http": "blacknode mcp --transport streamable-http --host 127.0.0.1 --port 9901 --path /mcp",
        },
        "nvidia": {
            "hosted_nim_env": "NVIDIA_API_KEY",
            "local_nim_env": "NGC_API_KEY",
            "node_names": [
                node_names[node_id]
                for node_id, meta in nodes.items()
                if _is_nvidia_node(meta)
            ],
            "model_refs": _model_refs(nodes),
        },
        "nodes": [
            {
                "step": index,
                "id": str(node_id),
                "name": node_names[str(node_id)],
                "label": _node_comment_label(str(node_id), meta),
                "type": str(meta.get("type")),
                "params": dict(meta.get("params") or {}),
                "inputs": list(meta.get("inputs") or []),
                "outputs": list(meta.get("outputs") or []),
                "upstream": _edge_refs(incoming.get(str(node_id), []), node_names, direction="upstream"),
                "downstream": _edge_refs(outgoing.get(str(node_id), []), node_names, direction="downstream"),
            }
            for index, (node_id, meta) in enumerate(nodes.items(), start=1)
        ],
        "edges": [
            {
                **edge,
                "from_name": node_names.get(str(edge.get("from")), str(edge.get("from"))),
                "to_name": node_names.get(str(edge.get("to")), str(edge.get("to"))),
            }
            for edge in raw_edges
        ],
    }


