def _incoming_edges(edges: list[dict[str, Any]]) -> dict[str, list[dict[str, Any]]]:
    incoming: dict[str, list[dict[str, Any]]] = {}
    for edge in edges:
        incoming.setdefault(str(edge.get("to")), []).append(edge)
    return incoming


def _outgoing_edges(edges: list[dict[str, Any]]) -> dict[str, list[dict[str, Any]]]:
    outgoing: dict[str, list[dict[str, Any]]] = {}
    for edge in edges:
        outgoing.setdefault(str(edge.get("from")), []).append(edge)
    return outgoing


def _edge_refs(
    edges: list[dict[str, Any]],
    node_names: Mapping[str, str],
    *,
    direction: str,
) -> list[dict[str, str]]:
    refs: list[dict[str, str]] = []
    for edge in edges:
        if direction == "upstream":
            node_id = str(edge.get("from"))
            port = str(edge.get("from_port", "output"))
            local_port = str(edge.get("to_port", "input"))
        else:
            node_id = str(edge.get("to"))
            port = str(edge.get("to_port", "input"))
            local_port = str(edge.get("from_port", "output"))
        refs.append({
            "id": node_id,
            "name": node_names.get(node_id, node_id),
            "port": port,
            "local_port": local_port,
        })
    return refs


def _is_nvidia_node(meta: Mapping[str, Any]) -> bool:
    type_name = str(meta.get("type") or "")
    return (
        type_name.startswith("NVIDIA")
        or type_name.startswith("NIM")
        or type_name in {"VideoFolderInput"}
        or any(str(value).lower().startswith("nim:") for value in (meta.get("params") or {}).values())
    )


def _model_refs(nodes: Mapping[str, Mapping[str, Any]]) -> list[str]:
    refs: list[str] = []
    for meta in nodes.values():
        params = meta.get("params") if isinstance(meta.get("params"), Mapping) else {}
        for key in ("model", "embedding_model", "rerank_model"):
            value = params.get(key) if isinstance(params, Mapping) else None
            if isinstance(value, str) and value and value not in refs:
                refs.append(value)
    return refs


def _unique_node_names(node_meta: Mapping[str, Mapping[str, Any]]) -> dict[str, str]:
    used: set[str] = {"workflow", "tasks", "agents", "handoffs", "node", "edge"}
    result: dict[str, str] = {}
    for index, (node_id, meta) in enumerate(node_meta.items(), start=1):
        base = _identifier(_node_name_source(str(node_id), meta)) or f"node_{index}"
        result[str(node_id)] = _dedupe(base, used)
    return result


def _node_name_source(node_id: str, meta: Mapping[str, Any]) -> str:
    type_name = str(meta.get("type") or "")
    if type_name in {"Output", "SubnetOutput"}:
        return "output"
    if _descriptive_node_id(node_id):
        return node_id
    return _node_label(meta) or node_id


def _node_comment_label(node_id: str, meta: Mapping[str, Any]) -> str:
    type_name = str(meta.get("type") or "Node")
    params = meta.get("params") if isinstance(meta.get("params"), Mapping) else {}
    if type_name == "Text":
        value = params.get("value") if isinstance(params, Mapping) else None
        if isinstance(value, str) and value.strip():
            return f"{type_name} {value.strip()!r}"
    label = _node_label(meta)
    if label and label != type_name:
        return f"{label} ({type_name})"
    return f"{type_name} ({node_id})"


def _node_label(meta: Mapping[str, Any]) -> str:
    params = meta.get("params") if isinstance(meta.get("params"), Mapping) else {}
    if isinstance(params, Mapping):
        for key in ("label", "name", "title"):
            value = params.get(key)
            if isinstance(value, str) and value.strip():
                return value.strip()
        type_name = str(meta.get("type") or "")
        value = params.get("value")
        if type_name == "Text" and isinstance(value, str) and value.strip() and len(value.strip()) <= 40:
            return f"{value.strip()} text"
    return str(meta.get("type") or "")


def _descriptive_node_id(node_id: str) -> bool:
    clean = _identifier(node_id)
    if len(clean) <= 2:
        return False
    if re.fullmatch(r"(node_?)?\d+", clean):
        return False
    if re.fullmatch(r"[a-z]\d*", clean):
        return False
    return True


def _dedupe(base: str, used: set[str]) -> str:
    name = base
    suffix = 2
    while name in used:
        name = f"{base}_{suffix}"
        suffix += 1
    used.add(name)
    return name


def _identifier(value: str) -> str:
    name = re.sub(r"\W+", "_", value.strip()).strip("_").lower()
    if not name:
        return ""
    if name[0].isdigit():
        name = f"node_{name}"
    if keyword.iskeyword(name):
        name = f"{name}_node"
    return name
